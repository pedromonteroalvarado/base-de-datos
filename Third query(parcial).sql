    CLIENTE_ID INT,
    MESA_ID INT,
    EMPLEADO_ID INT,
    PRODUCTO_ID INT,
    FECHA DATE,
    ESTADO VARCHAR(50),
    TOTAL DECIMAL(10, 2),
    FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTES(ID),
    FOREIGN KEY (MESA_ID) REFERENCES MESAS(ID),
